'''
str-string
int-inteiro
float-real
bool-boolena, sim ou não true/false
'''
print(type('luis'), 'luis') #indica a classe daquilo entre parenteses
print(type('10'), '10') #entre aspas é str
print(type(10), 10)
print(type(10.), 10.) #ele coloca o ,0
print(type(10==10), 10==10) #== é comparação
print(type(10=='L'), 10=='L')
print(bool('')) #vazio e 0 é falso
print(bool(0))
print(bool(1))
print(int(5.0)) #converte
print(float(4))
#CONCATENAÇÃO
print('10'+'10')
print('Lu'+'is')