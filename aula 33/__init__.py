#INDICES POSITIVOS
texto = "abcdefgh"
print(texto[3]) #uso para acessar uma letra

#INDICES NEGATIVOS
texto="hehe boy"
print(texto[-1]) #de tras pra frente
print(texto[:-2]) #fatio, tirou as 2 ultimas
print(texto[:2]) #mostra os 2 primeiros
print(texto[2:7]) #da onde inicia onde termina
print(texto[2:-2])
print(texto[2::2])